async function includePartials() {
  const includeTargets = document.querySelectorAll("[data-include]");

  await Promise.all([...includeTargets].map(async (target) => {
    const file = target.dataset.include;

    try {
      const response = await fetch(file);
      if (!response.ok) {
        throw new Error(`Could not load ${file}`);
      }
      target.outerHTML = await response.text();
    } catch (error) {
      // Fallback for local file:// usage. On GitHub Pages or a local web server, fetch() works normally.
      if (partialFallbacks[file]) {
        target.outerHTML = partialFallbacks[file];
      } else {
        console.warn(`Partial not loaded: ${file}`, error);
      }
    }
  }));
}

function setLanguage(lang) {
  const dictionary = translations[lang] || translations.de;

  document.documentElement.lang = lang;
  localStorage.setItem("tiantian-language", lang);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });
}

function setupInteractions() {
  document.getElementById("year").textContent = new Date().getFullYear();

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.lang));
  });

  const mobileToggle = document.getElementById("mobileToggle");
  if (mobileToggle) {
    mobileToggle.addEventListener("click", () => {
      document.body.classList.toggle("menu-open");
    });
  }

  document.querySelectorAll(".nav-links a").forEach((link) => {
    link.addEventListener("click", () => {
      document.body.classList.remove("menu-open");
    });
  });
}

document.addEventListener("DOMContentLoaded", async () => {
  await includePartials();

  setupInteractions();

  const savedLanguage = localStorage.getItem("tiantian-language") || "de";
  setLanguage(savedLanguage);
});
